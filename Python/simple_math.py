

#Addition operator --> + (shift + )
print("3+5") #output 1 ==> '3 + 5'
print("3"+"5") #output 2 ==> '35'
print(3+ 5)  #output 3 ==> 8

# NOTE ! ==> Only text should be placed in quotes, but when want to calculate arithmetic operations we should't place in the quoites.

#Subtraction Operator
print("3 - 5") #output 1 ==> '3 + 5'
print("3" - "5") #output 2 ==> error
print(3 - 5)  #output 3 ==> -2

#Multiplication
print("3 * 5") #out ==>3 * 5
print("3" * "5") # out ==> no calculate text
print(3 * "5") # out==> 555
print("hello"*3) #out==>hellohellohello

#Division

print("3 / 5") #out ==>3 / 5
print("3" / "5") # out ==> no calculate text, error
print("3" / "5") # out==> 0.6
print(1/2) #==>0.5
