# How can i print letters in string
# A: using index nunmbers i can print the letters
# Q: what are the range of index of string
# A: starts from 0 and goes to (len(string)-1)
st = input("Enter string please: ")
index = 0
while index <= (len(st)-1):
    print(st[index])
    index += 1

    
    


























